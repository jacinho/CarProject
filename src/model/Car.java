package model;

public class Car {

	private String brand;
	private Integer velocity;
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public Integer getVelocity() {
		return velocity;
	}
	public void setVelocity(Integer velocity) {
		this.velocity = velocity;
	}
	
}
